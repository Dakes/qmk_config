/* 
 rotaries 
 1: bottom
 2: top left
 3: top right
 */
void encoder_update_user(uint8_t index, bool clockwise)
{
    if (index == 0)
    { /* First encoder: Volume adjust*/
        if (clockwise)
        {
            tap_code(KC_AUDIO_VOL_UP);
        } else
        {
            tap_code(KC_AUDIO_VOL_DOWN);
        }
    } else if (index == 1)
    { /* Second encoder: Mouse wheel*/
        if (clockwise)
        {
            tap_code(KC_MS_WH_DOWN);
        } else
        {
            tap_code(KC_MS_WH_UP);
        }
    } else if (index == 2)
    { /* Third encoder: Next track and play by press*/
        if (clockwise)
        {
            tap_code(KC_MEDIA_NEXT_TRACK);
        } else
        {
            tap_code(KC_MEDIA_PREV_TRACK);
        }
    }
}

